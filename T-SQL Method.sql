DECLARE @RN INT;
DECLARE @C INT = 1;

WHILE @C< 3
BEGIN
 
   
		DECLARE CRS_stem CURSOR FOR 
		SELECT  INTCODE
		FROM tbl_mus_liste
		WHERE GRUP_KODU = 'G'+convert(varchar(1),@C)
		and SORUMLU IS NULL
		order by bal desc
		OPEN CRS_stem
		FETCH NEXT FROM CRS_stem INTO @RN
		WHILE @@FETCH_STATUS =0
		BEGIN

				DECLARE @A VARCHAR(25);
				DECLARE @B FLOAT;

				SELECT  @B = BAL
				FROM tbl_mus_liste WHERE INTCODE =@RN;

				SELECT @A=AGENT FROM (
				SELECT * , ROW_NUMBER ( ) OVER(PARTITION BY 1 ORDER BY BAKIYE ASC,ADET ASC,AGENT ) RN
				 FROM tbl_agent_liste
				 ) AS X WHERE X.RN=1;

				UPDATE tbl_mus_liste
				SET SORUMLU = @A
				WHERE INTCODE =@RN;

				UPDATE tbl_agent_liste
				SET BAKIYE = BAKIYE+@B,
					ADET=ADET+1
				WHERE AGENT =@A;
		  
		FETCH NEXT FROM CRS_stem INTO @RN
		END
		CLOSE CRS_stem
		DEALLOCATE CRS_stem;

SET @C = @C+1;
END;
		 
